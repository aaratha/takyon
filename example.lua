O = osc(0.1, 440, Saw)
X = osc(0.1, 220, Sine)
L1 = lfo(440, 220, 2)
L2 = lfo(2, 2, 1)
L3 = lfo(0.05, 0.05, 2, math.pi / 2, Square)
L4 = lfo(PI / 2, PI / 2, 0.25, 0, Sine)
L5 = lfo(1000, 700, 0.25)
F = filter(1000, 2)

sound(O)
	.freq(lfo(440, 220, 2).freq(L2))
	.amp(L3.shift(L4))
	.amp(L3.shift(L4))
	.amp(L3.shift(L4))
	.effect(F)
	.cutoff(L5)
	.play()
